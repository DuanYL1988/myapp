package com.app.form;

public class TeamEditForm {

    /**
     * 重复区分
     */
    private String coverClassify;

    private String moveType;

    private String color;

    private String race;

    private String skillA;

    private String skillB;

    private String skillC;

    private String blessType;

    public String getCoverClassify() {
        return coverClassify;
    }

    public void setCoverClassify(String coverClassify) {
        this.coverClassify = coverClassify;
    }

    public String getMoveType() {
        return moveType;
    }

    public void setMoveType(String moveType) {
        this.moveType = moveType;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getRace() {
        return race;
    }

    public void setRace(String race) {
        this.race = race;
    }

    public String getSkillA() {
        return skillA;
    }

    public void setSkillA(String skillA) {
        this.skillA = skillA;
    }

    public String getSkillB() {
        return skillB;
    }

    public void setSkillB(String skillB) {
        this.skillB = skillB;
    }

    public String getSkillC() {
        return skillC;
    }

    public void setSkillC(String skillC) {
        this.skillC = skillC;
    }

    public String getBlessType() {
        return blessType;
    }

    public void setBlessType(String blessType) {
        this.blessType = blessType;
    }

}
